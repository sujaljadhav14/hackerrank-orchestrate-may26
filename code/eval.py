"""
eval.py — Self-evaluation against sample_support_tickets.csv.

Reads both the agent's output.csv and the ground-truth sample CSV, then
computes exact-match accuracy on 'status' and 'request_type'.

Run:
    python code/eval.py

Optionally pass custom paths:
    python code/eval.py --predicted support_tickets/output.csv \
                        --ground-truth support_tickets/sample_support_tickets.csv
"""
from __future__ import annotations

import argparse
from pathlib import Path

import pandas as pd
from rich.console import Console
from rich.table import Table
from rich.panel import Panel

console = Console()

# Default paths (relative to repo root, which is one level above code/)
_REPO_ROOT = Path(__file__).parent.parent
_DEFAULT_PREDICTED = str(_REPO_ROOT / "support_tickets" / "output.csv")
_DEFAULT_GT = str(_REPO_ROOT / "support_tickets" / "sample_support_tickets.csv")


def _norm_issue(text: str) -> str:
    """Normalise an issue string for join key comparison."""
    return " ".join(str(text).lower().split())


def evaluate(predicted_path: str, ground_truth_path: str) -> dict:
    """
    Compare predicted output against the ground-truth sample CSV.

    Matching strategy:
      1. Join on normalised Issue text (exact after whitespace collapse).
      2. Fall back to positional slice if zero text matches are found
         (legacy behaviour when running against different ticket sets).

    Args:
        predicted_path:    Path to output.csv generated by main.py.
        ground_truth_path: Path to sample_support_tickets.csv.

    Returns:
        Dict with accuracy scores.
    """
    pred = pd.read_csv(predicted_path)
    gt   = pd.read_csv(ground_truth_path)

    # Normalise column names
    gt.columns   = [c.strip().lower().replace(" ", "_") for c in gt.columns]
    pred.columns = [c.strip().lower().replace(" ", "_") for c in pred.columns]

    if "issue" not in pred.columns or "issue" not in gt.columns:
        console.print("[red]ERROR:[/red] 'Issue' column missing from one of the CSVs.")
        return {}

    # ── Build join keys ───────────────────────────────────────────────────────
    pred["_key"] = pred["issue"].apply(_norm_issue)
    gt["_key"]   = gt["issue"].apply(_norm_issue)

    merged = gt.merge(pred, on="_key", suffixes=("_gt", "_pred"))

    if len(merged) == 0:
        console.print(
            "[yellow]Warning:[/yellow] No Issue-text matches found between predicted and "
            "ground-truth files.\n"
            "This usually means you ran [bold]main.py[/bold] on the full "
            "[cyan]support_tickets.csv[/cyan] (different tickets from the sample).\n\n"
            "For a meaningful self-evaluation, run the agent on the sample tickets first:\n"
            "  [bold]python code/main.py "
            "--input support_tickets/sample_support_tickets.csv "
            "--output support_tickets/sample_output.csv[/bold]\n"
            "  [bold]python code/eval.py "
            "--predicted support_tickets/sample_output.csv[/bold]\n"
        )
        return {}

    n = len(merged)

    status_col_pred = "status_pred" if "status_pred" in merged.columns else "status"
    status_col_gt   = "status_gt"   if "status_gt"   in merged.columns else "status"
    rt_col_pred     = "request_type_pred" if "request_type_pred" in merged.columns else "request_type"
    rt_col_gt       = "request_type_gt"   if "request_type_gt"   in merged.columns else "request_type"

    pred_status = merged[status_col_pred].str.strip().str.lower()
    gt_status   = merged[status_col_gt].str.strip().str.lower()
    pred_rt     = merged[rt_col_pred].str.strip().str.lower()
    gt_rt       = merged[rt_col_gt].str.strip().str.lower()

    status_acc = (pred_status == gt_status).mean()
    rtype_acc  = (pred_rt == gt_rt).mean()
    overall    = (status_acc + rtype_acc) / 2

    # ── Pretty output ──────────────────────────────────────────────────────
    console.print(Panel.fit(
        "[bold cyan]Self-Evaluation Results[/bold cyan]",
        border_style="cyan",
    ))

    table = Table(title=f"Evaluated on {n} rows", show_header=True, header_style="bold")
    table.add_column("Metric", style="cyan", width=30)
    table.add_column("Score", style="green", justify="right")
    table.add_row("Status accuracy (exact)", f"{status_acc:.1%}")
    table.add_row("Request-type accuracy (exact)", f"{rtype_acc:.1%}")
    table.add_row("Overall (mean)", f"{overall:.1%}")
    console.print(table)

    # ── Error breakdown ────────────────────────────────────────────────────
    wrong_mask_status = pred_status.values != gt_status.values
    wrong_mask_rt     = pred_rt.values != gt_rt.values

    # Helper: short label from the Issue text
    issue_labels = merged["_key"].str[:60].tolist()

    if wrong_mask_status.any():
        console.print(f"\n[yellow]Status mismatches ({wrong_mask_status.sum()}):[/yellow]")
        for i, wrong in enumerate(wrong_mask_status):
            if wrong:
                console.print(
                    f"  [{i}] [dim]{issue_labels[i]!r}[/dim]\n"
                    f"       predicted=[bold]{pred_status.iloc[i]}[/bold]  "
                    f"expected=[dim]{gt_status.iloc[i]}[/dim]"
                )

    if wrong_mask_rt.any():
        console.print(f"\n[yellow]Request-type mismatches ({wrong_mask_rt.sum()}):[/yellow]")
        for i, wrong in enumerate(wrong_mask_rt):
            if wrong:
                console.print(
                    f"  [{i}] [dim]{issue_labels[i]!r}[/dim]\n"
                    f"       predicted=[bold]{pred_rt.iloc[i]}[/bold]  "
                    f"expected=[dim]{gt_rt.iloc[i]}[/dim]"
                )

    return {
        "status_accuracy": status_acc,
        "request_type_accuracy": rtype_acc,
        "overall": overall,
        "n": n,
    }


def main():
    parser = argparse.ArgumentParser(
        description="Self-evaluate the support-triage agent against the sample CSV."
    )
    parser.add_argument(
        "--predicted",
        default=_DEFAULT_PREDICTED,
        help=f"Path to predicted output.csv (default: {_DEFAULT_PREDICTED})",
    )
    parser.add_argument(
        "--ground-truth",
        default=_DEFAULT_GT,
        help=f"Path to ground-truth CSV (default: {_DEFAULT_GT})",
    )
    args = parser.parse_args()

    evaluate(args.predicted, args.ground_truth)


if __name__ == "__main__":
    main()
